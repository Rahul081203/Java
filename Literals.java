public class Literals {
    public static void main(String[] args) {
        byte a=32 ;
        int b=56;
        long c=56666666666L; //we use postfix 'l' or 'L' to denote a long integer
        short d=87;
        float e=1.065f; //we use postfix 'f' or 'F' to denote a floating point integer
        double f=1.04044d; //we use postfix 'd' or 'D' to denote a double type number. double is the default data type for decimal containing numbers and therefore you may or may not specify it using 'd' or 'D'
        char g='c';
        boolean h=true;
        String str="helloworld"; //it is must to use double quotation marks to denote a string in java unlike python
        System.out.print(str);
        //you can directly type sout and press enter to automatically add System.out.println()
    }
    
}
